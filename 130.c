#include <stdio.h>

int main()
{
    int M[2][2],i,j;
    float A[2][2];
    
    for (i=0;i<2;i++){
        for(j=0;j<2;j++){
           scanf("%d",&M[i][j]);
        }}
    
        A[0][0]=((M[0][0])*(M[0][0])+(M[0][1])*(M[1][0]));
        A[0][1]=((M[0][0])*(M[0][1])+(M[0][1])*(M[1][1]));
        A[1][0]=((M[1][0])*(M[0][0])+(M[1][1])*(M[1][0]));
        A[1][1]=((M[1][0])*(M[0][1])+(M[1][1])*(M[1][1]));
        
        for (i=0;i<2;i++){
            for(j=0;j<2;j++){
                printf("%.3f ",A[i][j]);}
        printf("\n");}
        return 0;
}